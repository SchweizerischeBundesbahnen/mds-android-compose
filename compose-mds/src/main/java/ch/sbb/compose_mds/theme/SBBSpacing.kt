package ch.sbb.compose_mds.theme

import androidx.compose.ui.unit.dp

/**
 * Defines the standard SBB spacing values for Jetpack Compose.
 *
 * These values are based on the SBB Design System.
 *
 */
object SBBSpacing {
    /** Corresponds to 0dp. */
    val Zero = 0.dp

    /** Corresponds to 4dp. */
    val XXSmall = 4.dp

    /** Corresponds to 8dp. */
    val XSmall = 8.dp

    /** Corresponds to 12dp. */
    val Small = 12.dp

    /** Corresponds to 16dp. */
    val Medium = 16.dp

    /** Corresponds to 24dp. */
    val Large = 24.dp

    /** Corresponds to 32dp. */
    val XLarge = 32.dp
}
